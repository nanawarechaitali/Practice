package com.basics;
// Type casting is the way of converting data from one data type to another data type.
// This process of data conversion is  also known as type conversion.
// Note-> Type casting is not possible for Boolean data type.
// There are main two types of type casting
// 1) Implicit -> Lower to Higher       2) Explicit -> Higher to Lower
//// int -> double
// Byte->Short->Char->int->long->float->double



public class TypeCasting {
    public static void main(String[] args) {
        int a = 23;
        long b =34;
        double c = 56;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }

}
